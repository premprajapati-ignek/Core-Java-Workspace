package com.ignek.core.java.basics;

public class PrimitiveDatatypes {
    public static void main(String[] args) {
        //boolean
        boolean isAuthenticate = true;
        System.out.println(isAuthenticate);

        //byte
        byte verySmallNumber = 120;
        System.out.println(verySmallNumber);

        //char
        char firstLetter = 'P';
        System.out.println(firstLetter);

        //short
        short smallRangeInteger = 32000;
        System.out.println(smallRangeInteger);

        //int
        int midRangeInteger = 500000;
        System.out.println(midRangeInteger);

        //long
        long largeRangeInteger = 10050000;
        System.out.println(largeRangeInteger);

        //float
        float smallPrecison = 3.14f;
        System.out.println(smallPrecison);

        //double
        double largePrecision = 3.141592653589793;
        System.out.println(largePrecision);
    }
}
